
// Objects
const person = {
    name: "Andrej",
    age: 47,
    evaluaties: [7, 10, 9]
};

// the full object
console.log(person)

// dot-notation
console.log(person.name);
console.log(person.age);

//bracket-notation
console.log(person["name"]);
console.log(person["age"]);

//key-value pair
console.log(person.evaluaties);
